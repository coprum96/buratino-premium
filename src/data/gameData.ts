// Types
export interface Character {
  name: string;
  emoji: string;
  image?: string;
  emotions?: Record<string, string>;
}

export interface Choice {
  text: string;
  icon: string;
  wisdom?: number;
  coins?: number;
  next?: number;
  nextLevel?: boolean;
  nextPhase?: string;
}

export interface Dialogue {
  character: string;
  emotion?: string;
  text: string;
  redFlags?: string[];
  choices: Choice[];
  presentation?: PresentationSlide[];
  microscope?: MicroscopeData;
}

export interface PresentationSlide {
  title: string;
  content: string;
  image?: string;
  redFlags?: string[];
}

export interface MicroscopeData {
  title: string;
  smallPrintText: string;
  dangerousTerms?: { term: string; explanation: string }[];
}

export interface QuizQuestion {
  id?: string;
  text: string;
  options: string[];
  correct: number;
  explanation?: string;
  reward?: number;
}

export interface Level {
  id: number;
  title: string;
  subtitle: string;
  gradient: string;
  dialogues: Dialogue[];
  quiz: QuizQuestion[];
  miniGame?: string;
}

export interface ScenarioTestItem {
  text: string;
  correct: number;
  reward: number;
}

export interface EducationalSection {
  name: string;
  content: string;
}

export interface EducationalMaterial {
  id: number;
  title: string;
  sections: EducationalSection[];
}

